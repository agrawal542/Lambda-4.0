// Globals in NodeJS

// 1. __dirname -> It will give you the directory path of the current module.
console.log(__dirname);

// 2. require -> It is a function, this can help you to import code from other files.
console.log(require);

// 3. process -> It is an object, it gives you information about the current process.
console.log(process);

// 4. module -> It is an object, it gives you information about the current module.
console.log(module);